package restAssuredAutomation;

public class UserRequest {
	private String name;
	private String job;
	
	// Constructor
	public UserRequest() {
	}
	
	// Getters
	public String getName() {
		return name;
	}
	
	public String getJob() {
		return job;
	}
	
	// Setters
	public void setName(String name) {
		this.name = name;
	}
	
	public void setJob(String job) {
		this.job = job;
	}

}
